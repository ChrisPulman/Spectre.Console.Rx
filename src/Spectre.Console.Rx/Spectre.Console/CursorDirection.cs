namespace Spectre.Console.Rx;

/// <summary>
/// Represents cursor direction.
/// </summary>
public enum CursorDirection
{
    /// <summary>
    /// Moves cursor up.
    /// </summary>
    Up,

    /// <summary>
    /// Moves cursor down.
    /// </summary>
    Down,

    /// <summary>
    /// Moves cursor left.
    /// </summary>
    Left,

    /// <summary>
    /// Moves cursor right.
    /// </summary>
    Right,
}